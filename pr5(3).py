import math


def main(y,z):
    res = 0
    n = len(y)
    for i in range(1, n+1):
        a = ((z[n+1-i-1])**2) / 94
        b = (y[n+1-i-1])**3
        c = y[n+1-i-1]
        res += 29 * math.exp(a+b+c)**6
    return res


