import math


def main(x):
    res = 0
    n = len(x)
    for i in range(1, n+1):
        a = 13*x[n+1-i-1]
        b = 54*x[math.ceil(i/3)-1]**2
        c = 65
        res += 23 * (a - b - c)**3
    return 38 * res


